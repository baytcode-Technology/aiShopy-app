export function findCurrency(code: string): CurrencyOption | undefined {
  return CURRENCY_OPTIONS.find((c) => c.code === code.toUpperCase())
}

export function formatCurrencyLabel(code: string): string {
  const item = findCurrency(code)
  if (!item) return code
  return `${item.code} — ${item.name} (${item.symbol})`
}
